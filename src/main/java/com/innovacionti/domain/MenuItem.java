package com.innovacionti.domain;

import java.time.LocalDateTime;

public class MenuItem {
    private long id;
    private long restaurantId;
    private String name;
    private String description;
    private int price;
    private String imageUrl;
    private String mealType; // Desayuno / Almuerzo / Cena
    private String timeRange;
    private int availableUnits;
    private LocalDateTime publishedAt;
    private boolean featured;

    public MenuItem() {
    }

    public MenuItem(long id, long restaurantId, String name, String description, int price,
                    String imageUrl, String mealType, String timeRange, int availableUnits,
                    LocalDateTime publishedAt, boolean featured) {
        this.id = id;
        this.restaurantId = restaurantId;
        this.name = name;
        this.description = description;
        this.price = price;
        this.imageUrl = imageUrl;
        this.mealType = mealType;
        this.timeRange = timeRange;
        this.availableUnits = availableUnits;
        this.publishedAt = publishedAt;
        this.featured = featured;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getRestaurantId() {
        return restaurantId;
    }

    public void setRestaurantId(long restaurantId) {
        this.restaurantId = restaurantId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getMealType() {
        return mealType;
    }

    public void setMealType(String mealType) {
        this.mealType = mealType;
    }

    public String getTimeRange() {
        return timeRange;
    }

    public void setTimeRange(String timeRange) {
        this.timeRange = timeRange;
    }

    public int getAvailableUnits() {
        return availableUnits;
    }

    public void setAvailableUnits(int availableUnits) {
        this.availableUnits = availableUnits;
    }

    public LocalDateTime getPublishedAt() {
        return publishedAt;
    }

    public void setPublishedAt(LocalDateTime publishedAt) {
        this.publishedAt = publishedAt;
    }

    public boolean isFeatured() {
        return featured;
    }

    public void setFeatured(boolean featured) {
        this.featured = featured;
    }
}
